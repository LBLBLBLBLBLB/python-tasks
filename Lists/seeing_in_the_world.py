locations = ['Georgia', 'Germany', 'Great Britain', 'France', 'United States', 'Spain', 'Austria']
# original order
print(locations)
# sorted without modifying
print(sorted(locations))
print(locations)
print()

# reversed
locations.reverse()
print(locations)
print()

# reverse again
locations.reverse()
print(locations)
print()

locations.sort()
print(locations)

locations.sort(reverse=True)
print(locations)