def show_messages(messages):
    for message in messages:
        print(f'Message: {message}')


short_messages = [
    "Hello!",
    "How are you?",
    "I'm good, thank you.",
    "What's up?",
    "Not much, just chilling.",
    "Wanna hang out later?",
    "Sure, that sounds like fun!",
    "See you soon!",
    "Bye!",
    "Take care!"
]

show_messages(short_messages)